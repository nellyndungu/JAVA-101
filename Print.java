public class Print {
    public static void main(String[] args){
        //System is build in class in Java
        //Out is a member of System
        // Print - print line
        System.out.println("Java Script");

        // Print method does not add a new line.
        System.out.print("Java Script");
        System.out.println("Print method");

        // Printing numbers: No double quotes
        System.out.println(8);
    }
}